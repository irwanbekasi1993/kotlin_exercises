package kotlin_basic_type_exercises

class BasicOperations {

    fun defineInt(int:Int):Int{
        return int;
    }

    fun defineFloat(f:Float):Float{
        return f;
    }

    fun defineDouble(d:Double):Double{
        return d;
    }

    fun defineChar(ch:Char):Char{
        return ch;
    }

    fun defineString(str:String):String{
        return str
    }

    fun defineIntArr(intArr:IntArray){
        println(intArr[0])
    }

    fun defineMuteableList(list:MutableList<Int>){
        println(list[0])
    }

    fun defineList(list:List<Int>){
        println(list[0])
    }

}
